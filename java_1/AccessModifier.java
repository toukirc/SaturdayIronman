package com.upskill.java_1;

public class AccessModifier {
	
	public String name = "Toukir";               // Public - all class can access
	
	private int age = 32;                 // Private - only same class can access
	
	protected int ssn = 12345;            // Protected - classes in the same package and subclass                    
	
	String address = "Heritage Tower";    // default -  classes in the same package cab access
	

}
